Cypress.Commands.add('checklink', ($el, nscroll) => {
    const href = $el.attr('href')
    if (!href.includes('https://')) {
        if (nscroll) {
            cy.wrap($el).click({ scrollBehavior: false })
        }
        else {
            cy.wrap($el).click()
        }
        cy.url().should('include', href)
    }
})
